# كود ريان لحماية المستخدمين - نسخة 2026
def security_advisor(user_input):
    # المجموعة التي حذر منها ريان (كتيبة الإنذار)
    critical_chars = ['أ', 'ب', 'ت', 'ث', 'ج', 'ح', 'خ', 'A', 'B', 'C', 'D']
    
    # فحص الحرف الأول بناءً على "قاعدة ريان"
    first_char = user_input[0].upper()
    
    print(f"--- فحص الأمان لـ: ({user_input}) ---")
    
    if first_char in critical_chars:
        print("⚠️ تنبيه: الكلمة تبدأ بحروف مكشوفة، يفضل تغييرها لزيادة التشفير.")
    else:
        print("✅ أحسنت: هذا الاختيار يجعل تعقبك من قبل الأنظمة الخارجية أصعب.")

# تجربة "ريان" للكود لإثبات حسن النية للشركة
security_advisor("خالد") # سيظهر تنبيه
security_advisor("سالم") # سيظهر قبول